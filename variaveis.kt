fun main() {
    //Declarando váriavel String mutável
    var primeiroNome = "Flávio"
    
    //Declarando variável Int mutável
    var idade = 33
    
    //Declarando variável Boolean mutável
    var programador: Boolean = true 
    
    //Declarando váriavel Double mutável
    var peso: Double = 82.3
    
    //Declarando uma váriavel Imutável
    val pi = 3.141592
    
    println(primeiroNome)
    println(idade)
    println(programador)
    println(peso)
    println(pi)
}