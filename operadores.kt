fun main() {
    
    //Declarando Variaveis imutáveis do tipo String
    val primeiroNome = "Flávio "
    val ultimoNome = "Caruso"
    
    //concatenando strings
    val nomeCompleto = primeiroNome + ultimoNome
    println(nomeCompleto)
    
    //Declarando Variaveis imutáveis do tipo Int
    val n1 = 9
    val n2 = 5
    
    //Somando dois inteiros e dividindo por 2
    val media = (n1 + n2)/2
    println (media)
}
